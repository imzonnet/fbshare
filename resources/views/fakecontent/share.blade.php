            <p class="story-body__introduction">EDF has confirmed that its finance director has quit ahead of an
                expected final investment decision on the £18bn Hinkley Point nuclear power plant. </p>
            <p>Thomas Piquemal stepped down because he feared the project could jeopardise EDF's financial
                position, according to reports.</p>
            <p>EDF shares are trading 6.6% lower. </p>
            <p>Last month, Chris Bakken, the director of the project that could produce 7% of UK electricity by
                2025, said he was <a href="http://www.bbc.co.uk/news/uk-england-somerset-35482001"
                                     class="story-body__link">leaving to pursue other opportunities</a>.</p>
            <p>Jean-Bernard Levy, chairman and chief executive of EDF, said Mr Piquemal told him of the decision
                to leave last week and that he regretted the "haste" of Mr Piquemal's departure.</p>
            <p>The company's board is expected to finalise in April how it will fund the project after
                postponing the decision a number of times.</p>
            <p>Mr Levy said the board was studying the investment in Hinkley Point to ascertain the best way to
                finance the power plant. He added that EDF aimed to announce a final investment decision
                "soon".</p>
            <p>EDF has provisionally appointed Xavier Girre, who joined the company last year as finance
                director of its French business, as the group finance chief.</p>
            <hr class="story-body__line">
            <h2 class="story-body__crosshead">Analysis: Kamal Ahmed, BBC Economics Editor</h2>
            <p>At first take, it might appear the resignation of a company's finance director reveals a business
                in crisis.</p>
            <p>And EDF's share price fall this morning seems to bear that out.</p>
            <p>But, for supporters of the building of a new £18bn nuclear power station at Hinkley Point in
                Somerset - the world's most expensive power project - the resignation of Thomas Piquemal looks
                like clearing the way for the EDF board to give the final go-ahead for the scheme.</p>
            <p>Mr Piquemal was widely reported as being a trenchant critic of the costly nuclear project.</p>
            <p>His fate appears to have been sealed last week when the French government, which largely owns
                EDF, confirmed that it is still backing the scheme</p>
            <p>That came after the meeting between Francois Hollande and David Cameron - also an enthusiastic
                backer.</p>
            <p>On this occasion, political will has trumped any financial concerns. </p>
