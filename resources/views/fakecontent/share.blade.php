            <p>The company is also facing opposition from French union officials, who have suggested that
                investment in Hinkley Point C should be delayed until 2019.</p>
            <p>The <a href="http://www.bbc.co.uk/news/uk-england-somerset-35689784" class="story-body__link">CFE-CGC
                    Energy union said</a> there were problems with a similar reactor design in France that
                needed to be solved.</p>
            <p>Francois Raillot from the union which has a seat on EDF's board, said the company could sell some
                assets to finance Hinkley Point, but added: "It is a very bad time to do it because the price of
                energy is so low.</p>
            <p>"The state, which is a shareholder, will have to give [EDF] money to do it. But another problem
                is that the French state has no money."</p>
            <p>The new Hinkley plant was originally due to open in 2017, and it has come under fire for both its
                cost and delays to the timetable for building.</p>
            <p>The British government has also been criticised for <a
                        href="http://www.bbc.co.uk/news/business-22772441" class="story-body__link">guaranteeing
                    a price of £92.50 per megawatt hour of electricity</a> - more than twice the current cost -
                for the electricity Hinkley produces for 35 years.</p>
            <p>A UK Government spokesperson said: "We continue to fully support the project and President
                Hollande said himself on Thursday afternoon that it has the full support of the French
                Government." </p>
            <h2 class="story-body__crosshead">Financing issues</h2>
            <p>Tony Roulstone, from the Cambridge Nuclear Energy Centre, said the departure of Mr Piquemal was
                "a big blow".</p>
            <p>He said EDF was already facing a number of other financing issues, including a decision to buy a
                majority stake in Areva's nuclear reactor unit, adding: "They are committed to upgrading their
                existing power stations in France at a cost of €55bn over 15 years."</p>
            <p>"At a commercial level there is a way for the government to step aside but at a political level,
                this is part of the energy strategy," he said.</p>
            <p>Tom Greatrex, the chief executive of the Nuclear Industry Association, said: "What I think is
                vitally important is we get a decision on Hinkley, and then we get on with replacing our
                generation stock which is going off line.</p>
            <p>"We need new nuclear as part of that low carbon balance linked to the future. And Hinkley is an
                important part of that."</p>
            <p>Tom Burke, chairman of environmental think tank E3G, said there was now "a serious question mark"
                over the future of Hinkley Point.</p>
            <p>"You've now had two senior people leave within a month, both clearly having trouble convincing
                their colleagues to go ahead in the direction that their colleagues want to go."</p>
