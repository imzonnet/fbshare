            <hr class="story-body__line">
            <p>The project has been plagued by delays, but publicly the firm has insisted a decision to move
                forward is imminent. </p>
            <p>In October last year, <a href="http://www.bbc.co.uk/news/business-34587650"
                                        class="story-body__link">EDF agreed a deal</a> under which China General
                Nuclear Power Corporation (CGN) would pay a third of the cost of the £18bn project in exchange
                for a 33.5% stake.</p>
            <p>But according to reports, EDF is struggling to find the cash for its remaining 66.5% stake and is
                seeking help from the French government, which owns 84.5% of EDF.</p>
